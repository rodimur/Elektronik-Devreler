print("BJT ' nin karma parametresi degerlerini giriniz : ")
HI = float(input("HI = "))
HF = float(input("HF = "))
HO = float(input("HO = "))
HR = float(input("HR = "))
print()
# Karma parametreleri kullanarak ac hesaplamarini yapan modul
RP = RC * RL / (RC + RL)
RZ = HI - HF * HR * RP / (1 * HO * RP)
RB = R1 * R2 /(R1 * R2)
RI = RZ * RB / (RZ + RB)
if HO != 0 and HR != 0:
    RT = 1/(HO - HF * HR / (HI + RS))
else:
    RT = float(input("Not = 1E + 30 degerini giriniz/n RT = "))
RO = RT * RC / (RT + RC)
AI = (RB / (RB + RZ)) * (HF / (1 + HO * RP)) * (RC / RL)
if AI <0.000001 :
    AI = 0
AV = HI + RC / (HI + (HI * HO - HF * HR) * RC)
VI = RI * VS / (RI + RS)
VO = AV * VI
VL = AV * RL / (RP + RL)
print()
print(" AC analiz sonuclari ")
print()
