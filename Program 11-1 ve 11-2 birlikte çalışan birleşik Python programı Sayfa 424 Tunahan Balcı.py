RB = R1 * (R2 / (R1 + R2))
RP = RC * (RL / (RC + RL))
BB = R2 * CC / (R1 + R2)
IE = (BB - 0.7) * (beta + 1)/(RB + beta * (E1 + E2))
RE = 0.026 / IE
R3 = beta * (RE + E1)
RI = RB * (R3 / (RB + R3))
AV = -RC / (E1 + RE)
VI = VS * (R1 / (RI + RS))
VO = AV * VI
VL = VO * (RL / (RO + RL)) 
VM = CC - IE *(beta / beta + 1) * (RC + E1 + E2)
